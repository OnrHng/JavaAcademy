package academy.everyonecodes.java.week1.set1.exercise2;

public class SetNumbers {

    public static int sumInt(int num1, int num2) {
        return num1 + num2;
    }

    public static double sumDouble(double num1, double num2){
        return num1 + num2;
    }

    public static void main(String[] args) {
        System.out.println("The int sum is : " + sumInt(5, 7));
        System.out.println("The double sum is : " + sumDouble(5.04 , 7.96));

    }
}
